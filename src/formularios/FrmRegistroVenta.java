    private void CargaGrilla()
    {
        tablaDetalle=new DetalleVentaTableModel(lEVentaDetalle.recorrerRetornaLista());
        gridDetalle.setModel(tablaDetalle);
        
        gridDetalle.getColumnModel().getColumn(0).setPreferredWidth(30);
        gridDetalle.getColumnModel().getColumn(1).setPreferredWidth(350);
        gridDetalle.getColumnModel().getColumn(2).setPreferredWidth(30);
        gridDetalle.getColumnModel().getColumn(2).setPreferredWidth(30);
        gridDetalle.getColumnModel().getColumn(2).setPreferredWidth(30);
        gridDetalle.setAutoResizeMode(3);

    }

