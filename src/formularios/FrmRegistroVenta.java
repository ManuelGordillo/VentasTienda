    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        FrmBuscaProductos frmproducto=new FrmBuscaProductos(this, rootPaneCheckingEnabled);
        frmproducto.setVisible(true);
        if (frmproducto.getproductoModelSel()!=null)
        {
            int nroItem=0;
            
            DetalleVentaModel itemDetalle=new DetalleVentaModel();
            
            if (tablaDetalle.getRowCount()==0)
            {
                nroItem=1;
            }
            else
            {
                nroItem=tablaDetalle.getRowCount()+1;
            }
            
            
            itemDetalle.setIddetalle(nroItem);
            itemDetalle.setItem(nroItem);
            itemDetalle.setCantidad(1);
            itemDetalle.setPrecio(frmproducto.getproductoModelSel().getPrecio());
            itemDetalle.setProducto(frmproducto.getproductoModelSel());
            lEVentaDetalle.agregarFin(itemDetalle);
            CargaGrilla();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscaClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaClienteActionPerformed
        // TODO add your handling code here:
        FrmBuscaClientes frmcliente=new FrmBuscaClientes(this, rootPaneCheckingEnabled);
        frmcliente.setVisible(true);
        if (frmcliente.getclienteModelSel()!=null)
        {
            clienteModelSel=frmcliente.getclienteModelSel();
            txtNroDocCli.setText(clienteModelSel.getDni());
            txtCliente.setText(clienteModelSel.getNombreLargo());
        }        
    }//GEN-LAST:event_btnBuscaClienteActionPerformed
   
