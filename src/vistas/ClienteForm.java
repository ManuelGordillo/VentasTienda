    private void btnListEliminadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListEliminadosActionPerformed
        // TODO add your handling code here:
        modelo = new DefaultTableModel();
    modelo.setColumnIdentifiers(new Object[]{"Id", "DNI", "Nombres", "Direcci√≥n", "Estado"});

    Stack<Cliente> eliminados = dao.obtenerEliminados();
    for (Cliente c : eliminados) {
        String estadoTexto = c.getEstado().equals("1") ? "Activo" : "Inactivo";
        modelo.addRow(new Object[]{
            c.getId(),
            c.getDni(),
            c.getNombres(),
            c.getDireccion(),
            estadoTexto
        });
    }

    tblClientes.setModel(modelo);

    }//GEN-LAST:event_btnListEliminadosActionPerformed

