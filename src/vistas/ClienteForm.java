    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String dni = txtDni.getText();
    if (dao.eliminarPorDni(dni) > 0) {
        JOptionPane.showMessageDialog(this, "Cliente eliminado.");
        mostrarClientes();
        limpiarCampos();
    } else {
        JOptionPane.showMessageDialog(this, "No se encontr√≥ el cliente.");
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        Cliente c = new Cliente();
    c.setDni(txtDni.getText());
    c.setNombres(txtNombres.getText());
    c.setDireccion(txtDireccion.getText());
    c.setEstado(comboEstado.getSelectedIndex() == 1 ? "1" : "0");

    if (dao.agregar(c) > 0) {
        JOptionPane.showMessageDialog(this, "Cliente agregado.");
        mostrarClientes();
        limpiarCampos();
    } else {
        JOptionPane.showMessageDialog(this, "Error al agregar cliente.");
    }
    }//GEN-LAST:event_btnAgregarActionPerformed

